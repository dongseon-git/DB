-- MOD()함수 : 특정 숫자를 나눈 나머지값을 구함
-- MOD([나눗셈 될 숫자],[나눌 숫자])
-- CEIL : 주어진 숫자와 가장 근접한 큰 정수를 출력
SELECT
    MOD(15,6) AS "MOD01",
    MOD(10,2) AS "MOD02",
    MOD(11,2) AS "MOD03",
    CEIL(3.14)
FROM dual
;
--     MOD01      MOD02      MOD03
------------ ---------- ----------
--         3          0          1