--TO_CHAR
--TO_CHAR 숫자데이터 -> 문자열 변환
-- 출력형태 L999,999.99 원화표시,천단위구분,소숫점이하2자리표시
-- < 9 > 숫자의 한 자리를 의미함(빈 자리는 채우지 않음) 소숫점 뒷부분은 0으로 채움
-- < 0 > 빈 자리를 0으로 채움
SELECT
    sal,
    TO_CHAR(sal,'$999,999')       AS "SAL_$", --$표시(천단위구분됨)
    TO_CHAR(sal,'L999,999')       AS "SAL_L", --지역화폐단위(천단위구분됨)
    TO_CHAR(sal,'999,999.99')     AS "SAL_1", --소숫점이하 표시
    TO_CHAR(sal,'000,999,999.00') AS "SAL_2", -- 억단위까지 표시
    TO_CHAR(sal,'000999999.99')   AS "SAL_3" -- 천단위 기호 생략
FROM emp;

