-- REPLACE()함수
-- 특정 문자열 데이터에 포함된 문자를 다른 문자로 대체할 경우 사용
-- REPLACE('문자열'또는 컬럼, '문자1', '문자2')
--          문자1 : 찾을 문자  ,  문자2 : 바꿀(대체) 문자
-- 바꿀(대체)문자를 입력하지않으면 자동으로 빈칸
SELECT
    '010-1234-5678' AS "REPLACE_BEFORE",
    REPLACE('010-1234-5678','-',' ') AS REP,
    REPLACE('010-1234-5678','-') AS REP
FROM dual
;
--REPLACE_BEFOR     REP             REP        
---------------     -------------   -----------
--010-1234-5678     010 1234 5678   01012345678