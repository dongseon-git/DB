-- loan 테이블을 사용하여 1000번 지점의 대출 내역을 출력하되 
-- 대출일자, 대출종목코드, 대출건수, 대출총액, 누적대출금액을 아래와 같이 출력하세요.
SELECT
    L_date AS "대출일자",
    L_code AS "대출종목코드",
    L_qty AS "대출건수",
    L_total AS "대출총액",
    SUM(L_total) OVER(ORDER BY L_date,L_code) AS "누적대출금액"
FROM loan
WHERE L_store = 1000
;
--대출일자         대출종목코드       대출건수       대출총액     누적대출금액
----------        ----------    ----------    ----------     ----------
--20110101        100                    3          2400           2400
--20110102        102                    2          2000           4400
--20110102        105                    2          3000           7400
--20110103        100                    2          1600           9000
